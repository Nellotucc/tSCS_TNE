function [ OUT ] = float2Bin( IN )
% ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
% DESCRIPTION
%  - Convertis un float en 5 char (avec 7 bits utiles) pour envoie via la liaison serie 
%~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
% INPUT (unité SI)
%  - IN         Un float (single)
%~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
% OUTPUT (unité SI)
%  - 
%~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
% EXEMPLE 1
%
%~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
% TO DO
%  - 
%~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

    VALbin = dec2bin(typecast(single(IN), 'uint32'),32);
    
    VALbin2     = [ ['0',VALbin(1:7)];...
                    ['0',VALbin(8:8+6)];...
                    ['0',VALbin(15:15+6)];...
                    ['0',VALbin(22:22+6)];...
                    ['0',VALbin(29:end),'000']];
%     VALbin2     = [ ['0',VALbin(1:7)];...
%                     ['0',VALbin(8:8+6)];...
%                     ['0',VALbin(15:15+6)];...
%                     ['0',VALbin(22:22+6)];...
%                     ['0',VALbin(29:end),'000']];
    OUT         = uint8(bin2dec(VALbin2));

end


